		azerbaijanEnvComPgobj.enterValidIdNo(base.propp.getProperty(prefix+"valididno"));
		azerbaijanEnvComPgobj.selectregmonth(base.propp.getProperty(prefix+"dobmonth"));
		azerbaijanEnvComPgobj.selectregdate(base.propp.getProperty(prefix+"dobdate"));
		azerbaijanEnvComPgobj.selectregyear(base.propp.getProperty(prefix+"dobyear"));
		azerbaijanEnvComPgobj.enterBeName(base.propp.getProperty(prefix+"benificiary"));
		azerbaijanEnvComPgobj.selectPClang(base.propp.getProperty(prefix+"lan"));
		azerbaijanEnvComPgobj.selectBusiness(base.propp.getProperty(prefix+"relationship"));
		//azerbaijanEnvComPgobj.enterMFName(base.propp.getProperty(prefix+"mfname"));
		azerbaijanEnvComPgobj.selectPClang(base.propp.getProperty(prefix+"pclang"));		      
		azerbaijanEnvComPgobj.enterQApin(base.propp.getProperty(prefix+"qapin"));
		azerbaijanEnvComPgobj.enterReQapin(base.propp.getProperty(prefix+"reqapin"));
